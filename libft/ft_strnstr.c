#include "libft.h"

char	*ft_strnstr(const char *s1, const char *s2, size_t n)
{
	size_t		i;
	size_t		j;
	char		*p1;

	i = 0;
	p1 = (char *)s1;
	if (s2[i] == '\0')
		return (p1);
	while (i < n && s1[i] != '\0')
	{
		j = 0;
		while (i + j < n && s1[i + j] == s2[j] && s2[j] != '\0')
			j++;
		if (s2[j] == '\0')
			return (p1 + i);
		i++;
	}
	return (NULL);
}
